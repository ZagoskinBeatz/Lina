def get_cv_module_status() -> Dict[str, bool]:
    """
    Проверяет доступность CV-зависимостей.

    Импортирует модули CV и проверяет их capabilities.
    Безопасен: при ошибке возвращает все False.

    Returns:
        Dict с ключами: screenshot, ocr, gui
    """
    result = {"screenshot": False, "ocr": False, "gui": False}

    try:
        from lina.cv.scanner import ScreenScanner
        scanner = ScreenScanner()
        caps = scanner.get_capabilities()
        result["screenshot"] = caps.get("screenshot", False)
    except Exception:
        logger.debug("CV screenshot module unavailable", exc_info=True)

    try:
        from lina.cv.ocr import OCREngine
        ocr = OCREngine()
        caps = ocr.get_capabilities()
        result["ocr"] = caps.get("ocr", False)
    except Exception:
        logger.debug("CV OCR module unavailable", exc_info=True)

    try:
        from lina.cv.detector import GUIDetector
        det = GUIDetector()
        caps = det.get_capabilities()
        result["gui"] = caps.get("contour_detection", False)
    except Exception:
        logger.debug("CV GUI module unavailable", exc_info=True)

    return result


# ─── Model-Aware Context Strategy ─────────────────────────────────────────────

def get_context_strategy(tier: str) -> dict:
    """
    Возвращает стратегию контекста для модели.

    Phase 20.1: всегда возвращает full-стратегию.

    Args:
        tier: тип модели (игнорируется, всегда full)

    Returns:
        Dict со стратегией:
          - max_rag_chars: макс. длина RAG-контекста
          - max_rag_chunks: макс. число RAG-чанков
          - include_runtime: включать ли рантайм-секцию
          - system_prompt_type: "full"
          - verbose_allowed: разрешён ли verbose-режим
          - reasoning_depth: "deep"
          - max_context_ratio: макс. доля контекста (0.0-1.0)
    """
    return {
        "max_rag_chars": 3000,
        "max_rag_chunks": 5,
        "include_runtime": True,
        "system_prompt_type": "full",
        "verbose_allowed": True,
        "reasoning_depth": "deep",
        "max_context_ratio": 0.6,   # Макс 60% окна на контекст
    }


def trim_rag_context(
    context: str,
    max_chars: int,
    preserve_boundaries: bool = True,
) -> str:
    """
    Урезает RAG-контекст до заданного лимита.

    Сохраняет целостность чанков — не обрезает посреди строки.

    Args:
        context: Полный RAG-контекст.
        max_chars: Максимальная длина (символы).
        preserve_boundaries: Обрезать по границе строки.

    Returns:
        Урезанный контекст.
    """
    if not context or len(context) <= max_chars:
        return context

    trimmed = context[:max_chars]
    if preserve_boundaries:
        # Обрезаем на границе строки
        last_newline = trimmed.rfind('\n')
        if last_newline > max_chars * 0.5:  # Не менее 50%
            trimmed = trimmed[:last_newline]

    return trimmed
