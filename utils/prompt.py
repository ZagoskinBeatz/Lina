def build_runtime_section(
    active_tier: Optional[str] = None,
    full_loaded: bool = False,
    cache_size: int = 0,
    cv_enabled: bool = False,
    cv_modules: Optional[Dict[str, bool]] = None,
) -> str:
    """
    Строит динамическую секцию рантайма с актуальными данными системы.

    Вызывается при каждом запросе из engine._build_prompt() для
    актуального состояния CPU, RAM, Swap, модели и CV.

    Args:
        active_tier: "full" / None
        full_loaded: загружена ли полная модель
        cache_size: количество записей в кэше ответов
        cv_enabled: включён ли CV-модуль
        cv_modules: dict со статусами CV-зависимостей

    Returns:
        Отформатированная строка рантайм-секции.
    """
    if cv_modules is None:
        cv_modules = {}

    lines = ["### РАНТАЙМ"]

    # ── Системные ресурсы ──
    try:
        cpu_count = psutil.cpu_count(logical=True) or 0
        cpu_percent = psutil.cpu_percent(interval=None)
        mem = psutil.virtual_memory()
        ram_total = round(mem.total / (1024 ** 2))
        ram_free = round(mem.available / (1024 ** 2))
        ram_percent = mem.percent
        swap = psutil.swap_memory()
        swap_total = round(swap.total / (1024 ** 2))
        swap_used = round(swap.used / (1024 ** 2))
    except Exception:
        logger.debug("Failed to read system metrics, using defaults", exc_info=True)
        cpu_count = 0
        cpu_percent = 0.0
        ram_total = ram_free = 0
        ram_percent = 0.0
        swap_total = swap_used = 0

    lines.append(
        f"CPU: {cpu_count} лог. ядер, загрузка {cpu_percent:.0f}% | "
        f"RAM: {ram_free}/{ram_total} MB свободно ({ram_percent:.0f}% занято) | "
        f"Swap: {swap_used}/{swap_total} MB"
    )

    # ── Модели LLM ──
    if active_tier:
        lines.append(f"Активная модель: полная (full)")
    else:
        lines.append("Модель: не загружена (спит)")

    lines.append(f"Full: {'загружена' if full_loaded else 'не загружена'}")

    # ── Кэш ──
    if cache_size > 0:
        lines.append(f"Кэш ответов: {cache_size} записей")

    # ── CV ──
    cv_status = "активен" if cv_enabled else "отключен"
    screenshot_status = "да" if cv_modules.get("screenshot") else "нет"
    ocr_status = "да" if cv_modules.get("ocr") else "нет"
    gui_status = "да" if cv_modules.get("gui") else "нет"
    lines.append(
        f"CV: {cv_status} | Скриншоты: {screenshot_status} | "
        f"OCR: {ocr_status} | GUI-детекция: {gui_status}"
    )

    return "\n".join(lines)


